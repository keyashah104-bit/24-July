choice=int(input("Enter a number (1-7):"))

if choice==1:
    print("Monday")
elif choice==2:
    print("Tuesday")
elif choice==3:
    print("Wednesday")
elif choice==4:
    print("Thursday")
elif choice==5:
    print("Friday")
elif choice==6:
    print("Saturday")
elif choice==7:
    print("Sunday")

else:
    print("Invalid choice! Please enter a valid number between 1 and 7.")